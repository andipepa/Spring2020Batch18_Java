package day02_Variables;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\t\tHello");
        // \t means a paragraph space
        System.out.println("Cybertek School");
        System.out.println("Cybertec \nSchool");
        System.out.println("\nMy\nName \nIs \nAndi");
        // \n means new line, starts with a new line
        System.out.println("My favorite show is \"Game of thrones\"");
        System.out.println("My favorite books are \"Friendship\" \"lets go\"");
    // \" prints double quote
        System.out.println("\\");
        // \\prints the single slash
        System.out.println("   *");
        System.out.println("  ***");
        System.out.println(" *****");
        System.out.println("*******");
        System.out.println(" *****");
        System.out.println("  ***");
        System.out.println("   *");

        System.out.println("\"love you\"");

    }
}
